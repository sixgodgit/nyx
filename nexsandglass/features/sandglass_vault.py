def _tokenize(text: str) -> set:
    """通用分词：中文2字词+英文2+字母+英文3-4字符滑动窗口（n-gram）。
    不索引单汉字（高频噪音）。"""
    tokens = set()
    t = text.lower()
    # 英文词（2+字母）
    tokens.update(re.findall(r"[a-zA-Z0-9_]{2,}", t))
    # 英文字符滑动窗口——2到4字窗口，覆盖"sup/up/port/grou/oup"等子串
    eng = "".join(re.findall(r"[a-z0-9]", t))
    for n in (2, 3, 4):
        for i in range(len(eng) - n + 1):
            tokens.add(eng[i:i + n])
    # 中文2字词
    chars = "".join(re.findall(r"[\u4e00-\u9fff]", text))
    for i in range(len(chars) - 1):
        tokens.add(chars[i : i + 2])
    return {t for t in tokens if t}


def _query_tokens(text: str) -> set:
    """搜索分词：和_tokenize相同，但单字仅当输入只有一个中文字时才保留。"""
    tokens = _tokenize(text)
    chars = "".join(re.findall(r"[\u4e00-\u9fff]", text))
    if len(chars) != 1:
        tokens -= set(chars)  # 去单字
    return tokens


def _parse_line(line: str) -> tuple:
    """返回 (ts, sender, text) 或 (None, None, None)。
    V2.4.0: 明文存储，直接返回原文。"""
    if " | " not in line:
        return None, None, None
    parts = line.strip().split(" | ", 2)
    if len(parts) < 3:
        return None, None, None
    return parts[0], parts[1], parts[2].strip()


def _write_idx(idx):
    """Write idx dict to sandglass.idx file (atomic replace)."""
    tmp = _IDX + ".tmp"
    with open(tmp, "w", encoding="utf-8") as f:
        f.write("# AUTO-GENERATED BY NEXSANDGLASS — DO NOT EDIT\n")
        f.write("# token:line_number,line_number,...\n")
        f.write("# delete this file and run rebuild_index() to regenerate\n")
        f.write("\n")
        for token in sorted(idx):
            f.write(f"{token}:{','.join(map(str, sorted(set(idx[token]))))}\n")
    os.replace(tmp, _IDX)


def idx_search(query: str, limit: int = 100) -> list:
    """IDX搜索：语言感知分词→倒排→返回候选行号列表"""
    try:
        idx = _sync_index()
        if not idx:
            rebuild_index()
            idx = _sync_index()
        if not idx:
            return []
        from nexsandglass.features.sandglass_think import _tokenize_for_density
        tokens = _tokenize_for_density(query)
        if not tokens:
            return []
        candidate_lines = set()
        for token in tokens:
            if token in idx:
                candidate_lines.update(idx[token])
        results = []
        with open(_SANDGLASS, "r", encoding="utf-8") as f:
            for n, line in enumerate(f, 1):
                if n in candidate_lines:
                    ts, sender, text = _parse_line(line)
                    if ts and text:
                        results.append((n, ts, text))
                        if len(results) >= limit:
                            break
        return results
    except Exception:
        return []


def rebuild_index() -> int:
    """全量重建投石问路（基于解密后文本）。原子写。返回词条数。"""
    global _idx_cache
    _idx_cache = None
    try:
        if not os.path.exists(_SANDGLASS):
            return 0
        idx = {}
        with open(_SANDGLASS, "r", encoding="utf-8") as f:
            for n, line in enumerate(f, 1):
                ts, sender, text = _parse_line(line)
                if not ts:
                    continue
                for token in _tokenize(text):
                    idx.setdefault(token, []).append(n)

        _write_idx(idx)
        return len(idx)
    except Exception:
        logger.warning("sandglass: rebuild_index() failed", exc_info=True)
        return 0


def _sync_index() -> dict:
    """增量更新索引并返回内存 dict。利用缓存避免重复读盘。"""
    global _idx_cache, _idx_mtime

    try:
        if not os.path.exists(_SANDGLASS):
            _idx_cache = {}
            return {}

        total = 0
        with open(_SANDGLASS, "r", encoding="utf-8") as f:
            total = sum(1 for _ in f)

        # 缓存命中：mtime未变 + 最大行号 ≥ 总行数
        if _idx_cache is not None:
            try:
                cur_mtime = os.path.getmtime(_SANDGLASS)
                if cur_mtime != _idx_mtime:
                    _idx_cache = None  # 文件被修改过，清缓存
                    return {}  # 返回空，让调用方重建
            except Exception:
                pass  # mtime 不可用，沿用缓存
            if _idx_cache is not None:
                cached_max = 0
                for lines in _idx_cache.values():
                    if lines:
                        cached_max = max(cached_max, max(lines))
                if cached_max >= total and total > 0:
                    return _idx_cache

        # 读 idx
        idx = {}
        idx_max = 0
        if os.path.exists(_IDX):
            with open(_IDX, "r", encoding="utf-8") as f:
                for line in f:
                    if line.startswith("#") or ":" not in line:
                        continue
                    t, rest = line.strip().split(":", 1)
                    nums = [int(x) for x in rest.split(",") if x]
                    idx[t] = nums
                    if nums:
                        idx_max = max(idx_max, max(nums))

        if idx_max and total > 0 and idx_max > total:
            logger.warning(f"sandglass: idx行号({idx_max})超过沙漏总行数({total})，可能是手动编辑了idx文件。自动重建中...")
            return {}  # 返回空 → 触发 mmap 降级搜索。下次 rebuild_index() 会自动重建。

        if idx_max >= total:
            _idx_cache = idx
            return idx

        # 增量：追加新行
        with open(_SANDGLASS, "r", encoding="utf-8") as f:
            for n, line in enumerate(f, 1):
                if n <= idx_max:
                    continue
                ts, sender, text = _parse_line(line)
                if not ts:
                    continue
                for token in _tokenize(text):
                    idx.setdefault(token, []).append(n)

        # 原子写
        _write_idx(idx)

        # LRU驱逐——超过5000词条时清缓存重建（防止无限膨胀）
        if len(idx) > 5000:
            _idx_cache = {}
            return {}
        _idx_cache = idx
        try: _idx_mtime = os.path.getmtime(_SANDGLASS)
        except Exception: pass
        return idx

    except Exception:
        logger.warning("sandglass: _sync_index() failed", exc_info=True)
        return _idx_cache if _idx_cache else {}


